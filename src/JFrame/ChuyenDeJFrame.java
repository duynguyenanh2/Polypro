    public void load() {
        DefaultTableModel model = (DefaultTableModel) tblGridView.getModel();
        model.setRowCount(0);
        try {
            List<ChuyenDe> list = dao.select();
            for (ChuyenDe cd : list) {
                Object[] row = {
                    cd.getMaCD(),
                    cd.getTenCD(),
                    cd.getHocPhi(),
                    cd.getThoiLuong(),
                    cd.getHinh()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }    
    }
    
    public void insert() {
        if(checkloi()){
            ChuyenDe model = getModel();
            try {
                dao.insert(model);
                this.load();
                this.clear();
                DialogHelper.alert(this, "Thêm mới thành công!");
            } catch (Exception e) {
                DialogHelper.alert(this, "Thêm mới thất bại!");
            }
        }
    }
    
    public void update() {
        if(checkloi()){
            ChuyenDe model = getModel();
            try {
                dao.update(model);
                this.load();
                DialogHelper.alert(this, "Cập nhật thành công!");
            } catch (Exception e) {
                DialogHelper.alert(this, "Cập nhật thất bại!");
            }
        }
    }
    
    public void delete() {
        if (DialogHelper.confirm(this, "Bạn có muốn xóa hay không?")) {
            String macd = txtMaCD.getText();
            try {
                dao.delete(macd);
                this.load();
                this.clear();
                DialogHelper.alert(this, "Xóa thành công!");
            } catch (Exception e) {
                DialogHelper.alert(this, "Xóa thất bại!");
            }
        }
    }
    
    public void clear(){
        this.setModel(new ChuyenDe());
        this.setStatus(true);
        lblHinh.setIcon(null);
    }
    
    public void edit() {
        try {
            String macd = (String) tblGridView.getValueAt(this.index, 0);
            ChuyenDe model = dao.findByID(macd);
            if (model != null) {
                this.setModel(model);
                this.setStatus(false);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    public void setModel(ChuyenDe model) {
        txtMaCD.setText(model.getMaCD());
        txtTenCD.setText(model.getTenCD());
        txtThoiLuong.setText(String.valueOf(model.getThoiLuong()));
        txtHocPhi.setText(String.valueOf(model.getHocPhi()));
        txtMota.setText(model.getMoTa());
        lblHinh.setToolTipText(model.getHinh());
        if (model.getHinh() != null) {
            ImageIcon icon = new ImageIcon(ShareHelper.readLogo(model.getHinh()).getImage().getScaledInstance(lblHinh.getWidth(), lblHinh.getHeight(), Image.SCALE_SMOOTH));
            lblHinh.setIcon(icon);
        }else {
            lblHinh.setIcon(null);
        }
    }
    
    ChuyenDe getModel() {
        ChuyenDe model = new ChuyenDe();
        model.setMaCD(txtMaCD.getText());
        model.setTenCD(txtTenCD.getText());
        model.setThoiLuong(Integer.valueOf(txtThoiLuong.getText()));
        model.setHocPhi(Double.valueOf(txtHocPhi.getText()));
        model.setHinh(lblHinh.getToolTipText());
        model.setMoTa(txtMota.getText());
        return model;
    }
    
    void setStatus(boolean insertable) {
        txtMaCD.setEditable(insertable);
        btnInsert.setEnabled(insertable);
        btnUpdate.setEnabled(!insertable);
        btnDelete.setEnabled(!insertable);
        boolean first = this.index > 0;
        boolean last = this.index < tblGridView.getRowCount() - 1;
        btnFirst.setEnabled(!insertable && first);
        btnPrev.setEnabled(!insertable && first);
        btnLast.setEnabled(!insertable && last);
        btnNext.setEnabled(!insertable && last);
    }
    
    void selectImage() {
        JFileChooser fileChooser = new JFileChooser();
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            if (ShareHelper.saveLogo(file)) {
                // Hiển thị hình lên form
                ImageIcon icon = new ImageIcon(ShareHelper.readLogo(file.getName()).getImage().getScaledInstance(lblHinh.getWidth(), lblHinh.getHeight(), Image.SCALE_SMOOTH));
                lblHinh.setIcon(icon);
                lblHinh.setToolTipText(file.getName());
            }
        }
    }
    
    
    
    public boolean checkloi(){
        String macd = txtMaCD.getText();
        String hoten = txtTenCD.getText();
        double hp;
        int tl;
        String hinh = lblHinh.getToolTipText();
        
        if(macd.length() != 5){
            DialogHelper.alert(this, "Mã phải đúng 5 kí tự!");
            return false;
        }
        
       if(!hoten.matches("^[\\p{L} .'-]+$")){
           DialogHelper.alert(this, "Họ tên không hợp lệ");
           return false; 
       }
       
        try {
            hp = Double.parseDouble(txtHocPhi.getText());
            if(hp <0){
                DialogHelper.alert(this, "HP phải dương!");
                return false;
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "HP phải là số!");
            return false;
        }

        try {
            tl = Integer.parseInt(txtThoiLuong.getText());
            if (tl < 0) {
                DialogHelper.alert(this, "TL phải dương!");
                return false;
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "TL phải là số!");
            return false;
        }
        
        if(hinh == null){
            DialogHelper.alert(this, "Hình không để trống");
            return false;  
        }
        
        return true;
    }

